export function HandleAdd(num1, num2) {
    return Number(num1) + Number(num2);
}

export function HandleSub(num1, num2) {
    return Number(num1) - Number(num2);
}

export function HandleMul(num1, num2) {
    return Number(num1) * Number(num2);
}

export function HandleDiv(num1, num2) {
    return Number(num1) / Number(num2);
}
